			<strong><h4>White Box Penetration Testing</h4></strong>
			<p>This is a comprehensive testing, as tester has been provided with whole range of information about the systems and/or network such as Schema, Source code, OS details, IP address, etc. It is normally considered as a simulation of an attack by an internal source. It is also known as structural, glass box, clear box, and open box testing.</p>
			
			<p>White box penetration testing examines the code coverage and does data flow testing, path testing, loop testing, etc.</p>
			
			<strong><h4>Black Box Penetration Testing</h4></strong>
			<p>In black box penetration testing, tester has no idea about the systems that he is going to test. He is interested to gather information about the target network or system. For example, in this testing, a tester only knows what should be the expected outcome and he does not know how the outcomes arrives. He does not examine any programming codes.</p>
			
			<strong><h4>Gray Box Penetration Testing</h4></strong>
			<p>In this type of testing, a tester usually provides partial or limited information about the internal details of the program of a system. It can be considered as an attack by an external hacker who had gained illegitimate access to an organization's network infrastructure documents. </p>
